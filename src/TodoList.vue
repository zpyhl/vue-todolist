<template>
  <div>
    <div>
      <input type="text" v-model="inputValue">
      <button class="submit" @click="handleSubmit">提交</button>
    </div>
    <ul>
      <!-- <li v-for='(item,index) of list' :key='index' :index='index'>{{item}}</li> -->
      <todo-item v-for='(item,index) of list' :key='index' :index='index' :content='item' @delete='handleDelete'></todo-item>
    </ul>
  </div>
</template>
<script>
import TodoItem from './components/TodoItem.vue'
export default {
  components: {
    'todo-item':TodoItem
  },
  data:function(){
    return {
      inputValue:'',
      list:[],
    }
  },
  methods:{
    handleSubmit:function(){
      if(this.inputValue){
        this.list.push(this.inputValue);
        this.inputValue = ''
      }
    },
    handleDelete:function(index){
      this.list.splice(index,1);
    }
  }
}
</script>

